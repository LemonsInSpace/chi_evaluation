
#!/usr/bin/env python3
# -----------------------------------------------------------------------------
# TVLA Plotting Script
# -----------------------------------------------------------------------------
#
# This script loads precomputed TVLA statistics (t1–t4) and generates plots
# used for visualising statistical leakage behaviour across masking orders.
#
# Overview:
#   - Recursively scans experiment directories for TVLA output files
#   - Loads t-statistic arrays (t1.npy ... t4.npy)
#   - Plots all statistical orders on a single figure
#   - Marks peak values and overlays detection thresholds (±4.5)
#
# Input:
#   - NumPy arrays generated by the analysis script (tvla_stats.py)
#
# Output:
#   - PNG and PDF plots saved to OUTPUT_DIR
#
# Notes:
#   - This is a lightweight visualisation tool and does not perform any
#     statistical computation itself.
#   - Plotting multiple orders together is used to compare signal structure
#     and relative strength across statistical orders.
# -----------------------------------------------------------------------------
import pathlib
import numpy as np
import matplotlib.pyplot as plt
import re


# CONFIG


ROOT_DIR = pathlib.Path("<PATH>")
OUTPUT_DIR = pathlib.Path("<PATH>")

TVLA_FILES = [
    "t1.npy",
    "t2.npy",
    "t3.npy",
    "t4.npy"
]

TVLA_THRESHOLD = 4.5
FIG_DPI = 300

plt.style.use("seaborn-v0_8-whitegrid")

OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# Load χ runs
chi_data = {}
# Load all Chi experiment folders matching naming convention:
# chi_<order>/ containing TVLA outputs
for folder in ROOT_DIR.rglob("chi*"):

    if not folder.is_dir():
        continue

    match = re.search(r"chi(?:_old)?_(\d+)", folder.name.lower())
    if not match:
        continue

    order = int(match.group(1))

    if order not in chi_data:
        chi_data[order] = {}

    for tvla in TVLA_FILES:

        tfile = folder / tvla
        if not tfile.exists():
            continue

        chi_data[order][tvla] = np.load(tfile)

        print(f"Loaded Chi order {order} {tvla} from {folder}")


# Plot
plt.figure(figsize=(12,6))

for order in sorted(chi_data):

    for tvla, arr in chi_data[order].items():

        tvla_order = tvla.replace("t", "").replace(".npy", "")
        label = f"TVLA Order {tvla_order}"

        line, = plt.plot(arr, label=label, linewidth=1)
        color = line.get_color()

        peak_idx = np.argmax(np.abs(arr))
        peak_val = arr[peak_idx]

        plt.scatter(peak_idx, peak_val, color=color, s=70, zorder=3)

plt.axhline(TVLA_THRESHOLD, linestyle="--", color="black")
plt.axhline(-TVLA_THRESHOLD, linestyle="--", color="black")

plt.title("χ TVLA Results (Statistical Orders 1–4, Masking Order 2)")
plt.xlabel("Sample Index")
plt.ylabel("t-value")

plt.legend(ncol=2)

plt.tight_layout()

png = OUTPUT_DIR / "chi_all_tvla_orders.png"
pdf = OUTPUT_DIR / "chi_all_tvla_orders.pdf"

plt.savefig(png, dpi=FIG_DPI)
plt.savefig(pdf)

plt.close()

print("Saved:")
print(png)
print(pdf)
